def rc(t):
    R=[1,0,0,0,0,0,0,0]
    for i in range(0,t):
        R.insert(0,0);
        R[0]=R[0]^R[8]
        R[4]=R[4]^R[8]
        R[5]=R[5]^R[8]
        R[6]=R[6]^R[8]
        R=R[0:8]
    return R[0]

